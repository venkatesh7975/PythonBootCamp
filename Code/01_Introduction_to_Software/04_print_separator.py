# 4. Print Separator
# ==========================================
# CONCEPT: Customizing Output (sep)
# ==========================================
# Why? Sometimes you don't want a space between items.
# How? Use the `sep="..."` argument inside print().

print("A", "B", "C", sep="-") # Output: A-B-C
print("User", "Domain", "com", sep="@") # Output: User@Domain@com

