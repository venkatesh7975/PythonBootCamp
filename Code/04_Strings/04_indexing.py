# 4. Indexing
# ==========================================
# CONCEPT: Position
# ==========================================
# We can grab a single character using its position (Index).
# Computers start counting at 0!

s = 'Python'
# P  y  t  h  o  n
# 0  1  2  3  4  5

print(s[0]) # P
print(s[5]) # n
