# 1. Variables
# ==========================================
# CONCEPT: Storing Data
# ==========================================
# Variables are containers. You don't "declare" type in Python.
# Just assign a value.

x = 5             # Integer
name = "Python"   # String
price = 19.99     # Float

print(x)
print(name)
print(price)

# Why? To reuse data later without typing "Python" every time.
