# 2. Reassignment
# ==========================================
# CONCEPT: Variables Handling Change
# ==========================================
# Variables can change value (and type!) at any time.

x = 5
print(f"x is {x}")

x = 100
print(f"x is now {x}")

x = "Hello" # Changing type from int to str (Dynamic Typing)
print(f"x is now {x}")
