# 29. Truthy & Falsy
# ==========================================
# CONCEPT: Implicit Booleans
# ==========================================
# Most things are True.
# Falsy things: 0, 0.0, "", [], None, False.

print(bool(0))        # False
print(bool(1))        # True
print(bool("Hello"))  # True
print(bool(""))       # False (Empty string)
